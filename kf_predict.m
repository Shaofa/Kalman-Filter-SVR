%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%% 
% Kalman Filter 时间更新阶段
%
% 语法:
%   [x, P] =kf_predict(x,P,A,Q)
%   
% 输入参数：
%       x:  Nx1，上一时刻后验估计状态值
%       P:  NxN，上一时刻误差协方差矩阵
%       A:  NxN，过程转移矩阵
%       Q:  NxN，过程噪声协方差矩阵
%输出参数：
%       x:  Nx1，当前时刻先验估计状态值
%       P:  NxN，当前时刻误差协方差矩阵估计值
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%

function [x, P] =kf_predict(x,P,A,Q)
    x = A*x; 
    P = A * P * A' + Q;
end